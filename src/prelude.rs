//! 预导入模块 - 统一管理常用外部依赖和内部类型的重导出
//!
//! 使用方式: `use crate::prelude::*;`
//!
//! 这个模块提供了项目中最常用的类型和 trait 的统一导入点，
//! 减少每个文件中重复的 use 语句。

